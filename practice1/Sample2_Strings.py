student_name = "John Smith"
number_of_chars = len(student_name)
is_char_s_present = 'S' in student_name
print("Number of characters in student_name:", number_of_chars)
print("Is 'S' present in student_name?:", is_char_s_present)
first_letter = student_name[0]
last_letter = student_name[-1]
print("First letter of student_name:", first_letter)
print("Last letter of student_name:", last_letter)
revert_name = student_name[::-1]
print("Reverted student_name:", revert_name)